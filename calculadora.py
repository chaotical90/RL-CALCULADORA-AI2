# Função para soma
def soma(x, y):
    return x + y

# Função para subtração
def subtracao(x, y):
    return x - y

# Função para divisão
def divisao(x, y):
    if y != 0:
        return x / y
    else:
        return "Erro: Divisão por zero não é permitida."
# Função para multiplicação
def multiplicacao(x, y):
    return x * y
    
